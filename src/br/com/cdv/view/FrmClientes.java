    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // botao salvar
        Clientes obj = new Clientes();
        if (obj.buscaPorCpf(txtCpf.getText()).getCpf() == null) {
                obj = new Clientes(txtNome.getText(), txtRg.getText(), txtCpf.getText(), txtEmail.getText(), txtTelefone.getText(),
                    txtCelular.getText(), txtCep.getText(), txtEndereco.getText(), Integer.parseInt(txtNumero.getText()),
                    txtComplemento.getText(), txtBairro.getText(), txtCidade.getText(), cbUf.getSelectedItem().toString());

            obj.cadastrar(obj);

            new Utilitarios().LimpaTela(painel_dados);
        }else{
            JOptionPane.showMessageDialog(null, "Cliente ja esta cadastrado!");
        }


    }//GEN-LAST:event_btnSalvarActionPerformed


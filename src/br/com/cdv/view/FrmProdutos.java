    public void listar() {
        Produtos obj = new Produtos();
        List<Produtos> lista = obj.listarProdutos();
        DefaultTableModel dados = (DefaultTableModel) tabelaProdutos.getModel();
        dados.setNumRows(0);

        for (Produtos p : lista) {

            dados.addRow(new Object[]{
                p.getId(),
                p.getDescricao(),
                p.getValor_bruto(),
                p.getPreco(),
                p.getQtd_estoque(),
                p.getFornecedor().getNome()

            });

        }

    }

